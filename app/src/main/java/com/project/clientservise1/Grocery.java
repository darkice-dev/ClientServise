package com.project.clientservise1;

public class Grocery {
    public int image;
    public String name;

    public Grocery(String Name, int Image) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int Image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String Name) {
        this.name = name;
    }
}
